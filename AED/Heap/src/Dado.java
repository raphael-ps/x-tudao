
public class Dado {
	private int prioridade;
	private String algumaCoisa;
	
	public Dado(int prioridade, String algumaCoisa) {
		this.prioridade = prioridade;
		this.algumaCoisa = algumaCoisa;
	}

	public int getPrioridade() {
		return prioridade;
	}

	public void setPrioridade(int prioridade) {
		this.prioridade = prioridade;
	}

	public String getAlgumaCoisa() {
		return algumaCoisa;
	}

	public void setAlgumaCoisa(String algumaCoisa) {
		this.algumaCoisa = algumaCoisa;
	}
	
	
}
