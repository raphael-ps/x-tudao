
public class Dado {
	private int chave;
	private String algumaCoisa;
	
	public Dado(int chave , String algumaCoisa) {
		this.chave = chave ;
		this.algumaCoisa = algumaCoisa;
	}

	public int getChave() {
		return chave;
	}

	public void setChave(int chave ) {
		this.chave = chave ;
	}

	public String getAlgumaCoisa() {
		return algumaCoisa;
	}

	public void setAlgumaCoisa(String algumaCoisa) {
		this.algumaCoisa = algumaCoisa;
	}
	
	
}
